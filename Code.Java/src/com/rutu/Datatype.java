package com.rutu;

public class Datatype {

	public static void main(String[] args) {

		
		//primitive data type
		
		//byte type --> 1 byte of data = 8 bit
		byte studentage = 20;
		byte studentmarks = 99;
		System.out.println(studentage  + "  " +  studentmarks);	
		
		//short data type --> 2 byte of data = 16 bit
		short height = 145;
		System.out.println(height);
		
		//int data type --> 4 byte of data = 32 bit
		int  weight = 60;
		System.out.println(weight);
		
		//long data type --> 8 byte of data = 64 bit 
		long PhoneNo= 123456789;
		System.out.println(PhoneNo);
		
		// float data type --> 4 byte of data = 32 bit
		float marks =99;
		System.out.println(marks);
		
		// double data type --> 8 byte of data = 64 bit
		double stokes = 99.998;
		System.out.println(stokes);
		
		//character data type--> 2 byte of data = 16 bit
		char grade='A' ;
		System.out.println(grade);
		
		//boolean data type = 1 bit 
		boolean attendance= true;
		System.out.println(attendance);
		
		
		
		
		
		
	}

}
